//logical and operator  (&&) -> true if both sides are true, false otherwise

//&& operator picks the second true value i.e 1 && 4 will return 4. Both numbers are true. (0'zero') is considered a false value


// logical or operator (||) -> true if at least one side is true, false otherwise

//|| operator picks the first true value i.e 1 && 4 will return 1. Both numbers are true. (0'zero') is considered a false value

let num1 = 5
console.log (num1 > 3 && num1 > 7)
console.log (num1 > 3 || num1 >7)